// প্রশ্ন ব্যবস্থাপনা ইউটিলিটি
const QuestionManager = {
    // নির্দিষ্ট টপিক থেকে এলোমেলো প্রশ্ন নির্বাচন
    getRandomQuestions(chapter, topic, count = 20) {
        if (!quizData[chapter] || !quizData[chapter][topic]) return [];
        
        const allQuestions = [...quizData[chapter][topic].questions];
        const shuffled = allQuestions.sort(() => 0.5 - Math.random());
        return shuffled.slice(0, count);
    },

    // অপশন এলোমেলো করা
    shuffleOptions(question) {
        const shuffledOptions = [...question.options].sort(() => 0.5 - Math.random());
        return {
            ...question,
            options: shuffledOptions
        };
    },

    // কোয়িজের জন্য প্রশ্ন প্রস্তুত করা
    prepareQuizQuestions(chapter, topic, count = 20) {
        const randomQuestions = this.getRandomQuestions(chapter, topic, count);
        return randomQuestions.map(question => this.shuffleOptions(question));
    },

    // টপিকের তথ্য পাওয়া
    getTopicInfo(chapter, topic) {
        if (quizData[chapter] && quizData[chapter][topic]) {
            return quizData[chapter][topic];
        }
        return null;
    },

    // অধ্যায়ের সকল টপিক পাওয়া
    getAllTopics(chapter) {
        if (quizData[chapter]) {
            return Object.keys(quizData[chapter]);
        }
        return [];
    },

    // টপিকের জন্য প্রশ্ন সংখ্যা
    getTotalQuestions(chapter, topic) {
        if (quizData[chapter] && quizData[chapter][topic]) {
            return quizData[chapter][topic].questions.length;
        }
        return 0;
    }
};